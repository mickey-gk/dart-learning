// inheritance is extending properties of one class to another;
// dart allows single inheritance - from parent to childClass;
// you can not inherite properties of multiple parents classes to childClass;

// how to perform inheritance - use keyword extends
// e.i: class ParentClass { properties};
// class ChildClass extends ParentClass properties { content...}

// Terminology
// Parent Class: The class whose properties and methods are inherited by another class is called parent class.
// It is also known as base class or super class.

// Child Class: The class that inherits the properties and methods of another class is called child class.
// It is also known as derived class or sub class


// Advantages Of Inheritance In Dart
// It promotes reusability of the code and reduces redundant code.
// It helps to design a program in a better way.
// It makes code simpler, cleaner and saves time and money on maintenance.
// It facilitates the creation of class libraries.
// It can be used to enforce standard interface to all children classes.


// Single Inheritance - In this type of inheritance, a class can inherit from only one class.
// In Dart, we can only extend one class at a time.

// Multilevel Inheritance - In this type of inheritance, a class can inherit from another class and that class can also inherit from another class.
// In Dart, we can extend a class from another class which is already extended from another class.

// Hierarchical Inheritance - In this type of inheritance, parent class is inherited by multiple subclasses.
// For example, the Car class can be inherited by the Toyota class and Honda class.